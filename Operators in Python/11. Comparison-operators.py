# 2.
print("Equal to operator -")
print("y == 10 : ", y == 10)
print("y == 100 : ", y == 100)
print("(y+1000) == 100 : ", (y+1000) == 100)
"""
OUTPUT -
Equal to operator -
y == 10 :  False
y == 100 :  True
(y+1000) == 100 :  False
"""

# 3. 
print("Not equal to operator -")
print("y != 50 : ", y != 50)
print("y != 100 : ", y != 100)
print("(y+120) != y : ", (y+120) != y)
"""
OUTPUT -
Not equal to operator -
y != 50 :  True
y != 100 :  False
(y+120) != y :  True
"""

